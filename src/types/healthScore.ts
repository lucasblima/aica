/**
 * Health Score Types
 * Issue #144: [WhatsApp AI] feat: Automated Relationship Health Score Calculation
 *
 * Types for contact health scores, history tracking, alerts, and at-risk contacts.
 */

// ============================================================================
// HEALTH SCORE CORE TYPES
// ============================================================================

/**
 * Trend direction based on recent health score history
 */
export type HealthScoreTrend = 'improving' | 'stable' | 'declining' | 'new';

/**
 * Risk level based on health score value
 */
export type RiskLevel = 'critical' | 'high' | 'moderate' | 'healthy';

/**
 * Alert types generated by health score calculations
 */
export type HealthAlertType =
  | 'score_critical'      // Score dropped below 20
  | 'score_low'           // Score dropped below 40
  | 'rapid_decline'       // Score dropped 20+ points in 7 days
  | 'no_interaction'      // No messages in 30+ days
  | 'sentiment_negative'; // Sustained negative sentiment

/**
 * Calculation method for health score
 */
export type CalculationMethod = 'automated' | 'manual' | 'migration';

// ============================================================================
// HEALTH SCORE COMPONENTS
// ============================================================================

/**
 * Breakdown of health score by component
 * Total: frequency (0-25) + recency (0-25) + sentiment (0-20)
 *        + reciprocity (0-15) + depth (0-15) = 0-100
 */
export interface HealthScoreComponents {
  /** Message frequency score (0-25) */
  frequency_score: number;
  /** Recency of last message score (0-25) */
  recency_score: number;
  /** Average sentiment score (0-20) */
  sentiment_score: number;
  /** Balance of incoming/outgoing messages (0-15) */
  reciprocity_score: number;
  /** Conversation depth/diversity (0-15) */
  depth_score: number;
  /** Total calculated score (0-100) */
  total_score: number;
  /** Number of messages analyzed */
  messages_analyzed: number;
  /** Days since last message */
  days_since_last_message: number;
  /** Timestamp of calculation */
  calculated_at: string;
}

// ============================================================================
// HEALTH SCORE HISTORY
