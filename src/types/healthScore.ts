// ============================================================================
// HELPER FUNCTIONS
// ============================================================================

/**
 * Get risk level from health score
 */
export function getRiskLevel(score: number): RiskLevel {
  if (score < 20) return 'critical';
  if (score < 40) return 'high';
  if (score < 60) return 'moderate';
  return 'healthy';
}

/**
 * Get color for risk level
 */
export function getRiskColor(level: RiskLevel): string {
  switch (level) {
    case 'critical': return '#EF4444'; // red-500
    case 'high': return '#F97316';     // orange-500
    case 'moderate': return '#EAB308'; // yellow-500
    case 'healthy': return '#22C55E';  // green-500
  }
}

/**
 * Get color for health score
 */
export function getHealthScoreColor(score: number): string {
  return getRiskColor(getRiskLevel(score));
}

/**
 * Get trend icon name
 */
export function getTrendIcon(trend: HealthScoreTrend): string {
  switch (trend) {
    case 'improving': return 'TrendingUp';
    case 'declining': return 'TrendingDown';
    case 'stable': return 'Minus';
    case 'new': return 'Sparkles';
  }
}

/**
 * Get human-readable alert message
 */
export function getAlertMessage(alertType: HealthAlertType): string {
  switch (alertType) {
    case 'score_critical':
      return 'Relacionamento em estado crítico';
    case 'score_low':
      return 'Relacionamento precisa de atenção';
    case 'rapid_decline':
      return 'Queda rápida na qualidade do relacionamento';
    case 'no_interaction':
      return 'Sem interação há mais de 30 dias';
    case 'sentiment_negative':
      return 'Conversas recentes foram negativas';
  }
}
