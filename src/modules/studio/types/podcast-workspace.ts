/**
 * Podcast Workspace Types
 *
 * Defines the complete state and action system for the podcast workspace,
 * which manages the multi-stage workflow for podcast episode creation:
 * 1. Setup - Guest information and episode metadata
 * 2. Research - Dossier generation and custom sources
 * 3. Pauta - Script and topic management
 * 4. Production - Recording session
 *
 * Migration Note: Consolidated from _deprecated/modules/podcast/types/workspace.ts
 *
 * @see StudioState for parent container state
 * @see StudioProject for episode-level metadata
 */

