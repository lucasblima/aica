/**
 * Stage 1: Setup State
 * Captures guest information and episode metadata.
 */
export interface SetupState {
  /** Type of guest (affects dossier generation) */
  guestType: 'public_figure' | 'common_person' | null;

  /** Guest's full name */
  guestName: string;

  /** Reference for AI search (title, occupation) or manual occupation */
  guestReference: string;

  /**
   * Manual bio notes for common people (UI-only field).
   * NOT persisted to database - used only during setup flow.
   * The actual biography is stored in research.dossier.biography.
   */
  guestBio: string;

  /** Platform contact ID (from platform_contacts table) */
  guestContactId: string | null;

  /** Contact information */
  phone: string;
  email: string;

  /** Episode theme/topic */
  theme: string;

  /** Whether theme was auto-suggested or manually entered */
  themeMode: 'auto' | 'manual';

  /** Podcast season number */
  season: string;

  /** Recording location */
  location: string;

  /** Scheduled recording date (ISO format) */
  scheduledDate: string;

  /** Scheduled recording time */
  scheduledTime: string;

  // AI search state
  /** Whether AI search is in progress */
  isSearching: boolean;

  /** Results from guest profile search */
  searchResults: any | null;
}

/**
 * Stage 2: Research State
 * Manages dossier generation and custom research sources.
 */
export interface ResearchState {
  /** Generated guest dossier from AI */
  dossier: Dossier | null;

  /** User-provided custom research sources */
  customSources: WorkspaceCustomSource[];

  /** Whether dossier is being generated */
  isGenerating: boolean;

  /** Timestamp of last successful generation */
  lastGenerated: Date | null;

  /** Error message if generation failed */
  error: string | null;

  /** Deep research result (from wizard or manual deep research) */
  deepResearch: import('../types/studio').DeepResearchResult | null;
}

/**
 * Stage 3: Pauta (Script) State
 * Manages episode script, topics, and categories.
 */
export interface PautaState {
  /** List of topics for the episode */
  topics: Topic[];

  /** Categories for organizing topics */
  categories: TopicCategory[];

  /** Previously saved pautas for this episode */
  savedPautas: SavedPauta[];

  /** ID of currently active pauta */
  activePautaId: string | null;

  /** Whether pauta is being generated */
  isGenerating: boolean;
}

/**
 * Stage 4: Production State
 * Manages recording session state.
 */
export interface ProductionState {
  /** Whether recording is active */
  isRecording: boolean;

  /** Whether recording is paused */
  isPaused: boolean;

  /** Total recording duration in seconds */
  duration: number;

  /** When recording started */
  startedAt: Date | null;

  /** When recording finished */
  finishedAt: Date | null;

  /** Currently active topic ID */
  currentTopicId: string | null;

  /** Path to recording file */
  recordingFilePath: string | null;
}

