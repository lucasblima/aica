/**
 * Complete workspace state for podcast episode creation.
 *
 * IMPORTANT: This is a CHILD state managed within StudioState.
 * - StudioState.mode === 'WORKSPACE' activates this
 * - StudioState.currentProject contains episode metadata
 * - This state manages the multi-stage workflow
 *
 * @see StudioState for parent container
 */
export interface PodcastWorkspaceState {
  // Navigation
  /** Current active stage */
  currentStage: PodcastStageId;

  /** Stages that have been visited (for progress tracking) */
  visitedStages: PodcastStageId[];

  // Stage states
  setup: SetupState;
  research: ResearchState;
  pauta: PautaState;
  production: ProductionState;

  // Global metadata
  /** Episode ID (maps to StudioProject.id) */
  episodeId: string;

  /** Show ID (maps to StudioProject.showId) */
  showId: string;

  /** Show title (denormalized for display) */
  showTitle: string;

  // Save state
  /** Timestamp of last successful save */
  lastSaved: Date | null;

  /** Whether there are unsaved changes */
  isDirty: boolean;

  /** Whether async operation is in progress */
  isLoading: boolean;

  /** Error message if operation failed */
  error: string | null;
}

